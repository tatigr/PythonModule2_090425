import openpyxl

def generate_excel(filename, data):
    """Генерирует Excel-файл с заданными данными."""
    wb = openpyxl.Workbook()
    sheet = wb.active

    for row_index, row_data in enumerate(data, start=1):
        for col_index, cell_value in enumerate(row_data, start=1):
            sheet.cell(row=row_index, column=col_index, value=cell_value)

    wb.save(filename)

data = [
    ["Имя", "Возраст", "Город"],
    ["Анна", 25, "Москва"],
    ["Борис", 30, "Санкт-Петербург"],
    ["Виктория", 28, "Екатеринбург"],
    ["Григорий", 35, "Новосибирск"],
]

generate_excel("example.xlsx", data)
print("Файл example.xlsx сгенерирован.")


