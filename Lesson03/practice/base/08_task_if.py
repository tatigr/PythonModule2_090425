x = int(input("Введите х координату точки: "))
y = int(input("Введите y координату точки: "))
rx = int(input("Введите х координату центра круга: "))
ry = int(input("Введите y координату центра круга: "))
rad = int(input("Введите радиус круга: "))
if ((x - rx) ** 2 + (y - ry) ** 2) ** 0.5 <= rad:
    print("Да")
else:
    print("Нет")