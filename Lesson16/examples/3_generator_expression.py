# Пример 1: Генерация квадратов чисел от 1 до 3
# Для каждого числа 'x' в диапазоне 1, 2, 3 вычисляется его квадрат (x**2).
# Результатом является генератор, который выдает эти квадраты по запросу.
squares = (x ** 2 for x in range(1, 4))
print("Квадраты чисел:", list(squares))
# Вывод: Квадраты чисел: [1, 4, 9]

# Пример 2: Фильтрация четных чисел из списка
# Для каждого 'num' в списке 'numbers' проверяется, является ли он четным (num % 2 == 0).
# Если условие истинно, число включается в генерируемую последовательность.
numbers = [1, 2, 3, 4, 5]
even = (num for num in numbers if num % 2 == 0)
print("Четные числа:", list(even))
# Вывод: Четные числа: [2, 4]